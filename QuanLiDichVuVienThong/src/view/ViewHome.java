    //them khach hang moi
    private void themKH(){
        try {
            if(!listKH.isEmpty()) KhachHang.setStMa(listKH.get(listKH.size()-1).getMa()+1);
            
            KhachHang kh = new KhachHang(tenKH.getText(), diaChiKH.getText(), 
                    loaiKH.getSelectedItem().toString());
            
            listKH.add(kh);
            ThaoTacKH.writeFile(listKH);
            viewTableKH();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
