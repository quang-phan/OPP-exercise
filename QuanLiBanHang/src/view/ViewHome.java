    //hien thi bang khach hang
    private void viewTableKH(){
        DefaultTableModel model = (DefaultTableModel)jTableKH.getModel();
        
        model.setRowCount(0);
        
        for(int i = 0; i < listKH.size(); i++){
            model.addRow(new Object[]{listKH.get(i).getMa(), listKH.get(i).getHoTen(),
            listKH.get(i).getDiaChi(), listKH.get(i).getNhom()});
        }
    }
    
    //them khach hang vao file
    private void themKH(){
        if(!listKH.isEmpty()) KhachHang.setStMa(listKH.get(listKH.size()-1).getMa()+1);
        
        try {
            KhachHang kh = new KhachHang(tenKH.getText(), diaChiKH.getText(),
                    nhomKH.getSelectedItem().toString());
            listKH.add(kh);
            ThaoTacKH.writeFile(listKH);
            viewTableKH();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
