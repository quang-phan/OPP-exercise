    //them mot dich vu moi
    private void themDV() {
        try {
            DichVu dv = ThaoTacDV.tim(listDV, tenDV.getText());
            if(dv != null){
                dv.sua(Double.parseDouble(giaCuocDV.getText()), donViTinh.getText());
            }
            else{
                if(!listDV.isEmpty()) DichVu.setStMa(listDV.get(listDV.size()-1).getMa()+1);
                
                dv = new DichVu(tenDV.getText(), Double.parseDouble(giaCuocDV.getText()), 
                        donViTinh.getText());
                listDV.add(dv);
                
            }
            ThaoTacDV.writeFile(listDV);
            viewTableDV();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }

    //hien thi danh sach dich vu
    private void viewTableDV() {
        DefaultTableModel model = (DefaultTableModel)jTableDV.getModel();
        
        model.setRowCount(0);
        
        for(int i = 0; i < listDV.size(); i++){
            model.addRow(new Object[]{listDV.get(i).getMa()+"",listDV.get(i).getTen(),
            listDV.get(i).getGiaCuoc()+"",listDV.get(i).getDonViTinh()});
        }
    }

    //them hoac sua hoa don
    private void themHD() {
        KhachHang kh = ThaoTacKH.tim(listKH, maKH2.getText());
        DichVu dv = ThaoTacDV.tim(listDV, maDV2.getText());
        if(dv != null && kh != null){
            HoaDon hd = ThaoTacHD.tim(listHD, kh);
            if(hd != null){
                try {
                    hd.them(dv, Integer.parseInt(soLuong.getText()));
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, e.getMessage());
                }
            }
            else{
                try {
                    hd = new HoaDon(kh, dv, Integer.parseInt(soLuong.getText()));
                    listHD.add(hd);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, e.getMessage());
                }
            }
            ThaoTacHD.writeFile(listHD);
            phanTach();
            viewTableHD();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "khong tim khach hang hoac dich vu sai");
        }
        
    }
    
