    //hien thi tong tien phai tra
    private void viewTableTK(){
        double sum = 0;
        DefaultTableModel model = (DefaultTableModel)jTableTK.getModel();
        
        model.setRowCount(0);
        
        for(int i = 0; i < listHD.size(); i++){
            List<MatHang> mhs = listHD.get(i).getMatHang();
            List<Integer> integers = listHD.get(i).getSoLuong();
            
            for (int j = 0; j < mhs.size(); j++) {
                sum += mhs.get(j).getDonGia()*integers.get(j);
            }
            
            model.addRow(new Object[]{listHD.get(i).getKhachHang().getMa()+"",
            listHD.get(i).getKhachHang().getHoTen(), listHD.get(i).getKhachHang().getDiaChi(),
            listHD.get(i).getKhachHang().getNhom(), sum+""});
        }
    }
    
    //them hoa don
    private void themHD(){
        KhachHang kh = ThaoTacKH.tim(listKH, tenKH2.getText());
        MatHang mh = ThaoTacMH.tim(listMH, tenMH2.getText());
        if(mh != null && kh != null){
            HoaDon hd = ThaoTacHD.tim(listHD, kh);
            if(hd != null){
                hd.them(mh, Integer.parseInt(soLuong.getText()));
            }
            else{
                hd = new HoaDon(kh, mh, Integer.parseInt(soLuong.getText()));
                listHD.add(hd);
            }
            ThaoTacHD.writeFile(listHD);
            phanTach();
            vieWTableHD();
        }
        else JOptionPane.showMessageDialog(rootPane, "thong tin khach hang hoac mat hang sai");
    }
    
