    //hien thi thong tin cong nhan ra bang
    private void viewTableCN() {
        DefaultTableModel model = (DefaultTableModel) jTableCN.getModel();

        model.setRowCount(0);
        for (int i = 0; i < listCN.size(); i++) {
            model.addRow(new Object[]{"" + listCN.get(i).getMa(), listCN.get(i).getHoTen(),
                listCN.get(i).getDiaChi(), listCN.get(i).getCaSX()});
        }
    }

    //doc danh sach san pham
