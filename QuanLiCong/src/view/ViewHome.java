    private void themPCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themPCActionPerformed
        // TODO add your handling code here:
        CongNhan cn = ThaoTacNV.tim(listCN, maCN2.getText());
        SanPham sp = ThaoTacSP.tim(listSP, maSP2.getText());
        int x = Integer.parseInt(soluong.getText());
        
        if(cn != null && sp != null && x >= 0){
            
            Cong cong = ThaoTacPC.kiemTra(ListPC, cn);
            if(cong != null){
                
                if(!cong.themSanPham(sp, x))JOptionPane.showMessageDialog(rootPane, "nhan vien co toi da loai san pham");
                
            }
            else{
                cong = new Cong();
                cong.themCongNhan(cn, sp, x);
                ListPC.add(cong);
            }
            
            
            ThaoTacPC.writeFile(ListPC);
            phanTach();
            viewTablePC();
        }else JOptionPane.showMessageDialog(rootPane, "thong tin nhap sai");
    }//GEN-LAST:event_themPCActionPerformed

