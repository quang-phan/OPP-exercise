    private void thongKe() {
        DefaultTableModel model = (DefaultTableModel)jTableTK.getModel();
        
        model.setRowCount(0);
        double sum = 0;
        
        for(int i = 0; i < listHD.size(); i++){
            sum = 0;
            List<DichVu> dvs = listHD.get(i).getListDV();
            List<Integer> list = listHD.get(i).getListsSL();
            
            for(int j = 0; j < dvs.size(); j++){
                sum += dvs.get(j).getGiaCuoc()*list.get(j);
            }
            
            model.addRow(new Object[]{listHD.get(i).getKh().getMa(), listHD.get(i).getKh().getTen(),
            listHD.get(i).getKh().getDiaChi(), listHD.get(i).getKh().getLoai(), sum+""});
        }
    }
