    private void lamMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lamMoiActionPerformed
       double tongThu = 0;
       DefaultTableModel model = (DefaultTableModel)jTableThuNhap.getModel();
       model.setRowCount(0);
       for(int i = 0; i < ListPC.size(); i++){
           Map<SanPham, Integer> map = ListPC.get(i).getSanPham();
           tongThu = 0;
           for(Map.Entry<SanPham, Integer> entry : map.entrySet()){
               tongThu  += entry.getKey().getDonGia()*entry.getValue();
           }
           model.addRow(new Object[]{ListPC.get(i).getCongNhan().getMa()+"", ListPC.get(i).getCongNhan().getHoTen(),
           tongThu+""});
       }
    }//GEN-LAST:event_lamMoiActionPerformed

    //them mot nhan vien
    private void themCN() {
        if (!listCN.isEmpty()) {
            CongNhan.setStMa(listCN.get(listCN.size() - 1).getMa() + 1);
        }
        Pattern pattern = Pattern.compile("\\w+");
        Matcher matcher = pattern.matcher(tenNV.getText()),
                matcher1 = pattern.matcher(diaChiNV.getText());
        if (matcher.find() && matcher1.find()) {
            CongNhan cn = new CongNhan(tenNV.getText(), diaChiNV.getText(),
                    caSX.getSelectedItem().toString());
            listCN.add(cn);
            ThaoTacNV.writeFile(listCN);
            viewTableCN();
        }
    }

